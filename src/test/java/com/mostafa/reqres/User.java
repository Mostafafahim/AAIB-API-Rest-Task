package com.mostafa.reqres;

public class User {
        private String name;
        private String initialJob;
        private String updatedJob;

        // Getters and Setters
        public String getName() { return name; }
        public void setName(String name) { this.name = name; }

        public String getInitialJob() { return initialJob; }
        public void setInitialJob(String initialJob) { this.initialJob = initialJob; }

        public String getUpdatedJob() { return updatedJob; }
        public void setUpdatedJob(String updatedJob) { this.updatedJob = updatedJob; }
}
